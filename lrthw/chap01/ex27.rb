#Exercise 27 - The truth terms

#In Ruby we have the following terms (characters and phrases) for determining if something
# is "true" or "false." Logic on a computer is all about seeing if some combination of these characters and some variables is true at that point in the program.

&& - (and)
|| - (or)
! - (not)
!= - (not equal)
== (equal)
>= (greater-than-equal)
<= (less-than-equal)
true
false
